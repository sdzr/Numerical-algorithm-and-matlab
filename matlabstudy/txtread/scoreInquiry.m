% --- Executes just before scoreInquiry is made visible.
function scoreInquiry_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to scoreInquiry (see VARARGIN)

% Choose default command line output for scoreInquiry
handles.output = hObject;
%打开文件
fin=fopen('studentScore.txt','r');
%读取文件第一行
str=fgetl(fin);
[str1 str2 str3 str4]=strread(str,'%s %s %s %s','delimiter',' ');
xingming(1)=str1;
counter=2;
%一行一行的读取文件
while feof(fin)==0
    str=fgetl(fin);
    [name yuwen shuxue yingyu]=strread(str,'%s %d %d %d','delimiter',' ');
    xingming(counter)=name;
    studentScore(counter-1,:)=[yuwen shuxue yingyu];
    counter=counter+1;
end
%让文件在listbox1中显示出来
set(handles.listbox1,'string',xingming);
%让studentScore在其他函数中也能调用
handles.studentScore=studentScore;
%关闭文件
fclose(fin);
% Update handles structure
guidata(hObject, handles);

